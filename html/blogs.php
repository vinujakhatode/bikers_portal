                                    <span class="dots7" id="dots7">...</span><span id="more7" class="more7">That is why it is very important to take precautions in those moments of reduced visibility.
                                    To do this, take note of these tips that will help you ride a motorcycle at night much more safely :
                                    <br>     
Check the motorcycle lights: Many times we get confused and do not realize that the lights of our motorcycle do not work or do so improperly. Position, low and long lights must work and be properly leveled. Let’s not forget the turn signals, the brake light, and the license plate.
<br>No to smoked visors: Smoked visors, provided they are approved, are very useful to protect you from reflections and the incidence of the sun. However, when night comes, it prevents us from seeing clearly and is very dangerous. In addition, you can be fined. It is better to use a fully transparent screen.
<br>Reflective or light-colored equipment: The motorcycle itself is difficult to be perceived by other vehicles. If normally it is already advisable to use light-colored equipment, even more, if we circulate at night or in conditions of low visibility. In the market, there are helmets, vests, and clothes with reflective colors, without a doubt the best option to be seen on the road.
<br>Increase safety distances: Low visibility can cause unexpected reaction times (braking, lane changes …) to be longer. Therefore, increase your safety distance with the vehicle in front.
<br>Reference paint and road beacons: The darkness itself or even the glare from the headlights of the vehicles that come from the front makes us often not see the road asphalt. A good practice is to look at the line of paint that delimits the lane of the road or the beacons of the right margin so as not to lose the references.
<br>Increase the frequency of your stops: At night, the body tends to relax more easily and, therefore, fatigue and drowsiness may appear more quickly. Rest what is necessary and shorten the times between your stops.
